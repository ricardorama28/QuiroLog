export function findProcedureInKnowledgeBase(name: string): ProcedureTemplate | undefined {
  const lower = name.toLowerCase().trim()
  return procedureKnowledgeBase.find((p) => {
    if (p.name.toLowerCase().includes(lower) || lower.includes(p.name.toLowerCase())) return true
    return p.aliases?.some((a) => a.toLowerCase().includes(lower) || lower.includes(a.toLowerCase()))
  })
}
